        // Teknik özellikler HTML'i oluşturma
        function generateSpecsHTML(specs) {
            let html = '';
            Object.keys(specs).forEach(key => {
                const title = key.charAt(0).toUpperCase() + key.slice(1);
                html += `
                    <div class="spec-category">
                        <h4>${title.replace(/([A-Z])/g, ' $1').trim()}</h4>
                        <p>${specs[key]}</p>
                    </div>
                `;
            });
            return html;
        }

        // Teknik özellikler toggle fonksiyonu
        function toggleSpecs(index) {
            const specsContent = document.getElementById(`specs-${index}`);
            const icon = document.getElementById(`icon-${index}`);
            
            if (currentOpenSpec !== null && currentOpenSpec !== index) {
                const prevSpecs = document.getElementById(`specs-${currentOpenSpec}`);
                const prevIcon = document.getElementById(`icon-${currentOpenSpec}`);
                prevSpecs.classList.remove('active');
                prevSpecs.style.maxHeight = '0px';
                prevIcon.style.transform = 'rotate(0deg)';
            }
            
            if (specsContent.classList.contains('active')) {
                specsContent.classList.remove('active');
                specsContent.style.maxHeight = '0px';
                icon.style.transform = 'rotate(0deg)';
                currentOpenSpec = null;
            } else {
                specsContent.classList.add('active');
                
                requestAnimationFrame(() => {
                    const fullHeight = specsContent.scrollHeight;
                    const extraPadding = 40;
                    const finalHeight = fullHeight + extraPadding;
                    specsContent.style.maxHeight = finalHeight + 'px';
                });
                
                icon.style.transform = 'rotate(180deg)';
                currentOpenSpec = index;
            }
        }

