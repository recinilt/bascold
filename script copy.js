        function updateLanguageSelector() {
            const currentFlag = document.getElementById('currentFlag');
            const currentLang = document.getElementById('currentLang');
            const options = document.querySelectorAll('.language-option');
            
            // Mevcut dili güncelle
            switch(currentLanguage) {
                case 'tr':
                    currentFlag.className = 'flag-icon flag-tr';
                    currentFlag.textContent = 'TR';
                    currentLang.textContent = 'Türkçe';
                    break;
                case 'en':
                    currentFlag.className = 'flag-icon flag-en';
                    currentFlag.textContent = 'EN';
                    currentLang.textContent = 'English';
                    break;
                case 'ar':
                    currentFlag.className = 'flag-icon flag-ar';
                    currentFlag.textContent = 'ع';
                    currentLang.textContent = 'العربية';
                    break;
            }
            
            // Seçeneklerdeki aktif durumu güncelle
            options.forEach(option => {
                option.classList.remove('active');
                if (option.onclick.toString().includes(`'${currentLanguage}'`)) {
                    option.classList.add('active');
                }
            });
        }

        function updateDirection() {
            if (currentLanguage === 'ar') {
                document.body.setAttribute('dir', 'rtl');
                document.documentElement.setAttribute('dir', 'rtl');
            } else {
                document.body.setAttribute('dir', 'ltr');
                document.documentElement.setAttribute('dir', 'ltr');
            }
        }

        function translatePage() {
            const elements = document.querySelectorAll('[data-translate]');
            elements.forEach(element => {
                const key = element.getAttribute('data-translate');
                if (translations[currentLanguage] && translations[currentLanguage][key]) {
                    if (element.innerHTML.includes('<br>')) {
                        element.innerHTML = translations[currentLanguage][key];
                    } else {
                        element.textContent = translations[currentLanguage][key];
                    }
                }
            });

            // Title etiketlerini güncelle
            const titleElements = document.querySelectorAll('[data-translate-title]');
            titleElements.forEach(element => {
                const key = element.getAttribute('data-translate-title');
                if (translations[currentLanguage] && translations[currentLanguage][key]) {
                    element.setAttribute('title', translations[currentLanguage][key]);
                }
            });

            // Sayfa başlığını güncelle
            updatePageTitle();
        }

